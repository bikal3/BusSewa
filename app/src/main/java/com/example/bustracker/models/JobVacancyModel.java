package com.example.bustracker.models;

public class JobVacancyModel {
    private String id,title,experience,required_number,location,vdate,employe_type,description,salary;

    public String getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }

    public String getExperience() {
        return experience;
    }

    public String getRequirednumber() {
        return required_number;
    }

    public String getLocation() {
        return location;
    }

    public String getVdate() {
        return vdate;
    }

    public String getEmploye_type() {
        return employe_type;
    }

    public String getDescription() {
        return description;
    }

    public String getSalary() {
        return salary;
    }
}
